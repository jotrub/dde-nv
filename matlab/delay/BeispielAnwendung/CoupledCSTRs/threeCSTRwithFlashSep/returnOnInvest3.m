defineROIWeightingNCSTR

J1=@(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10,x11,x12,...
    Fr,beta,...
    T01,F01,Tc1,V1,...
    T02,F02,Tc2,V2,...
    T03,F03,Tc3,V3,...
    TcF,VF)...
    -gamma1*(F01/3600+F02/3600+F03/3600)*x11; % production

J2=@(x1,x2,x3,x4,x5,x6,x7,x8,x9,x10,x11,x12,...
    Fr,beta,...
    T01,F01,Tc1,V1,...
    T02,F02,Tc2,V2,...
    T03,F03,Tc3,V3,...
    TcF,VF)...
    gamma2*(F01/3600+F02/3600+F03/3600)*1; % educt input

