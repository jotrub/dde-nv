    Fr,beta,...
    T01,F01,Tc1,V1,...
    T02,F02,Tc2,V2,...
    T03,F03,Tc3,V3,...
    TcF,VF)...
    gamma3*beta/10*((V1/1000)^(2/3)*(Tc1-x3)+(V2/1000)^(2/3)*(Tc2-x6)+(V3/1000)^(2/3)*(Tc3-x9)+(VF/1000)^(2/3)*(TcF-x12)); % heat input

